
package karyawan;


public class Karyawan {
protected String nama,idKerja,jabatan;
protected boolean status;
protected int anak,tahunMasuk,gaji;


    public Karyawan(String nama, String idKerja, String jabatan, boolean status, int anak, int tahunMasuk,int gaji) {
        this.nama = nama;
        this.idKerja = idKerja;
        this.jabatan = jabatan;
        this.status = status;
        this.anak = anak;
        this.tahunMasuk = tahunMasuk;
        this.gaji=gaji;
    }
    

    
    
}
